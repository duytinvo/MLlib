class TransT5(T5PreTrainedModel):
    def __init__(self, config, **kwargs):
        super().__init__(config)
        num_labels = kwargs.pop("num_labels", None)
        encoder_layers = kwargs.pop("encoder_layers", None)
        decoder_layers = kwargs.pop("decoder_layers", None)
        freeze_encoder_embs = kwargs.pop("freeze_encoder_embs", False)

        self.model_dim = config.d_model

        self.shared = nn.Embedding(config.vocab_size, config.d_model)
        if freeze_encoder_embs:
            print("Fixed pre-trained embeddings")
            self.shared.weight.requires_grad = False

        encoder_config = copy.deepcopy(config)
        if encoder_layers is not None:
            print("Number of encoder layers %d" % encoder_layers)
            encoder_config.num_layers = encoder_layers
        self.encoder = T5Stack(encoder_config, self.shared)

        decoder_config = copy.deepcopy(config)
        decoder_config.is_decoder = True
        if decoder_layers is not None:
            print("Number of decoder layers %d" % decoder_layers)
            decoder_config.num_layers = decoder_layers
        # Use different embeddings at decoder
        if num_labels is not None:
            self.vocab_size = num_labels
            self.decoder_embs = nn.Embedding(self.vocab_size, config.d_model)
        else:
            self.vocab_size = config.vocab_size
            self.decoder_embs = self.shared

        self.decoder = T5Stack(decoder_config, self.decoder_embs)

        self.lm_head = nn.Linear(config.d_model, self.vocab_size, bias=False)

        self.init_weights()

    def freeze_encoder_embeddings(self):
        print("Fixed pre-trained embeddings")
        self.shared.weight.requires_grad = False
        self.encoder.embed_tokens.weight.requires_grad = False

